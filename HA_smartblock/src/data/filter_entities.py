DENY_DOMAINS = {
    "update",
    "conversation",
    "event",
    "todo",
    "zone",
    "tts",
    "weather",
}

# ------------------------------------------------------------
# Helpers
# ------------------------------------------------------------

def safe_domain(entity_id: str) -> str:
    if not entity_id or "." not in entity_id:
        return ""
    return entity_id.split(".", 1)[0].strip()

def simple_name(entity: Dict[str, Any]) -> str:
    attrs = entity.get("attributes") or {}
    return attrs.get("friendly_name") or entity.get("entity_id") or ""

def slim_attributes(attrs: Dict[str, Any]) -> Dict[str, Any]:
    """너무 무거워지지 않게 자동화/블록 UI에 쓸만한 것만 남김.
    (원하면 그냥 attrs 전체를 반환해도 됨)
    """
    if not isinstance(attrs, dict):
        return {}

    keep_keys = [
        "friendly_name",
        "device_class",
        "unit_of_measurement",
        "supported_features",
        "supported_color_modes",
        "color_mode",
        "assumed_state",
        "options",
    ]
    out = {}
    for k in keep_keys:
        if k in attrs:
            out[k] = attrs[k]
    return out

def should_include(domain: str) -> bool:
    if not domain:
        return False
    if domain in DENY_DOMAINS:
        return False
    if domain in ALLOW_DOMAINS:
        return True
    return False

def js_escape(s: str) -> str:
    return s.replace("\\", "\\\\").replace("`", "\\`")

# ------------------------------------------------------------
# Load
# ------------------------------------------------------------

def load_states(path: str) -> List[Dict[str, Any]]:
    if not os.path.exists(path):
        raise FileNotFoundError(f"Input JSON not found: {path}")

    with open(path, "r", encoding="utf-8") as f:
        data = json.load(f)

    if not isinstance(data, list):
        raise ValueError("Input JSON must be a list (Home Assistant get_states result).")

    return data

# ------------------------------------------------------------
# Build output data
# ------------------------------------------------------------

def build_entities(raw: List[Dict[str, Any]]) -> List[Dict[str, Any]]:
    out: List[Dict[str, Any]] = []

    for e in raw:
        entity_id = e.get("entity_id", "")
        domain = safe_domain(entity_id)

        if not should_include(domain):
            continue

        attrs = e.get("attributes") or {}
        item = {
            "entity_id": entity_id,
            "domain": domain,
            "name": simple_name(e),
            "state": e.get("state"),
            "attributes": slim_attributes(attrs),
        }
        out.append(item)

    # 정렬: domain -> entity_id
    out.sort(key=lambda x: (x.get("domain", ""), x.get("entity_id", "")))
    return out

# get_states만으론 notify 디바이스 목록이 안 나오는 경우가 많아서
# 일단 빈 배열로 두고, 나중에 get_services까지 붙이면 자동 생성 가능.
def build_notify_devices(_: List[Dict[str, Any]]) -> List[str]:
    return []

# ------------------------------------------------------------
# Generate JS files
# ------------------------------------------------------------

def write_test2_entities_js(path: str, entities: List[Dict[str, Any]], notify_devices: List[str]) -> None:
    payload = {
        "dummyEntities": entities,
        "notifyDevices": notify_devices,
    }

    # JS 모듈로 export
    content = (
        "// AUTO-GENERATED by filter_entities.py\n"
        "// Input : test_entities.json (HA get_states raw dump)\n"
        "// Output: test2_entities.js (safe test file)\n\n"
        "export const dummyEntities = "
        + json.dumps(payload["dummyEntities"], ensure_ascii=False, indent=2)
        + ";\n\n"
        "export const notifyDevices = "
        + json.dumps(payload["notifyDevices"], ensure_ascii=False, indent=2)
        + ";\n"
    )

    with open(path, "w", encoding="utf-8") as f:
        f.write(content)

