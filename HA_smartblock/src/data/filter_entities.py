# ------------------------------------------------------------

def main():
    raw = load_states(IN_JSON)
    entities = build_entities(raw)
    notify_devices = build_notify_devices(raw)

    os.makedirs(DATA_DIR, exist_ok=True)

    write_test2_entities_js(OUT_ENTITIES_JS, entities, notify_devices)
    write_test2_options_js(OUT_OPTIONS_JS)

    print("✅ Done!")
    print("  input :", IN_JSON)
    print("  out   :", OUT_ENTITIES_JS)
    print("  out   :", OUT_OPTIONS_JS)
    print(f"  entities exported: {len(entities)}")
    print("  notifyDevices exported:", len(notify_devices))

if __name__ == "__main__":
    main()
